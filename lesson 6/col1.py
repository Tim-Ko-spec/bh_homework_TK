#Запросить трижды ввод наименования товаров и их цену через пробел. 
#"пример: 
#>>>яблоко 10"
#>>>груша 15
#>>>малина 20
    
#    - создать из введенных данных словарь где ключ это наименование, а цена значение
#    - запросить имя товара, найти его в словаре, и вывести его цену, увеличенную на 15%. 
#    - вывести сумму всех товаров

products = {}

# Запрашиваем трижды ввод товара и цены
for i in range(3):
    entry = input(f"Введите товар и цену через пробел (пример: яблоко 10): ")
    name, price = entry.split()
    products[name] = int(price)

search_item = input("Введите название товара для проверки цены: ")

# Если товар есть в словаре — увеличиваем цену на 15%
if search_item in products:
    new_price = products[search_item] * 1.15
    print(f"Цена товара '{search_item}' с увеличением на 15%: {new_price}")
else:
    print(f"Товар '{search_item}' не найден.")

# Выводим сумму 
total_sum = sum(products.values())
print("Сумма всех товаров:", total_sum)