#Создать структуру данных сотрудников фирмы с 
#    тремя сотрудниками. каждый сотрудник должен иметь:
#        ФИО, 
#        должность, 
#        год рождения, 
#        список навыков, 
#        список детей с их именем и годом рождения. 
    
#    Запросить ФИО сотрудника и вывести по нему информацию.
    
#    ** задать вопрос о желании добавить сотрудника,
#        если ответ да - добавить сотрудника через несколько input
#        (после добавления сотрудника вывести всю структуру консоль)

employees = {
    "Иванов Иван Иванович": {
        "должность": "Менеджер",
        "год рождения": 1985,
        "навыки": ["управление", "коммуникация"],
        "дети": [
            {"имя": "Петр", "год рождения": 2010},
            {"имя": "Анна", "год рождения": 2015}
        ]
    },
    "Петров Петр Петрович": {
        "должность": "Программист",
        "год рождения": 1990,
        "навыки": ["Python", "SQL", "Git"],
        "дети": []
    },
    "Сидоров Сидор Сидорович": {
        "должность": "Дизайнер",
        "год рождения": 1988,
        "навыки": ["Photoshop", "Illustrator"],
        "дети": [
            {"имя": "Мария", "год рождения": 2012}
        ]
    }
}

fio = input("Введите ФИО сотрудника: ")

if fio in employees:
    print("Информация о сотруднике:")
    for key, value in employees[fio].items():
        print(f"{key}: {value}")
else:
    print("Сотрудник не найден.")

add_new = input("Хотите добавить нового сотрудника? (да/нет): ").strip().lower()

if add_new == "да":
    fio_new = input("Введите ФИО: ")
    position = input("Введите должность: ")
    birth_year = int(input("Введите год рождения: "))
    skills = input("Введите навыки через запятую: ").split(",")
    
    children = []
    add_children = input("Есть ли дети? (да/нет): ").strip().lower()
    if add_children == "да":
        count = int(input("Сколько детей?: "))
        for i in range(count):
            child_name = input(f"Имя ребёнка {i+1}: ")
            child_year = int(input(f"Год рождения ребёнка {i+1}: "))
            children.append({"имя": child_name, "год рождения": child_year})
    
    employees[fio_new] = {
        "должность": position,
        "год рождения": birth_year,
        "навыки": [skill.strip() for skill in skills],
        "дети": children
    }

    print("\nОбновлённая структура сотрудников:")
    for name, info in employees.items():
        print(f"\n{name}:")
        for key, value in info.items():
            print(f"  {key}: {value}")