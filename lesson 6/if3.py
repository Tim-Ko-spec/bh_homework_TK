#апросить 3 числа. Вывести наибольшее из них. Решить используя if.

# Запрос трёх чисел
a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))
c = int(input("Введите третье число: "))

# Определяем наибольшее с помощью if
max_num = a
if b > max_num:
    max_num = b
if c > max_num:
    max_num = c

# Вывод результата
print("Наибольшее число:", max_num)