#Запросить у пользователя число
#    - если число менее 20 -  вывести на экран сколько чисел 
#        в диапазоне от 0 до этого числа делится без остатка на 7. 
#    - если более 20 - вывести на экран сколько чисел 
#        в диапазоне от 0 до этого числа делится без остатка на 11.

# Запрос числа у пользователя
num = int(input("Введите число: "))

if num < 20:
    # Считаем количество чисел, делящихся на 7
    count = len([x for x in range(num + 1) if x % 7 == 0])
    print("Количество чисел, делящихся на 7:", count)

elif num > 20:
    # Считаем количество чисел, делящихся на 11
    count = len([x for x in range(num + 1) if x % 11 == 0])
    print("Количество чисел, делящихся на 11:", count)

else:
    print("Число равно 20 — условие не подходит ни под один вариант.")