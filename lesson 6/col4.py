#Буква "a" стоит 10 очков, "b" - 20, "c" - 30, "d" - 40
#Запросить кодовою фразу из пяти символов используя только a, b, c, d.
#Вывести на экран общее количество очков введенной фразы.

points = {
    "a": 10,
    "b": 20,
    "c": 30,
    "d": 40
}

# Запрос кодовой фразы
phrase = input("Введите кодовую фразу из 5 символов (только a, b, c, d): ")

# Проверка длины и допустимых символов
if len(phrase) != 5 or not all(ch in points for ch in phrase):
    print("Ошибка! Фраза должна состоять ровно из 5 символов: a, b, c, d.")
else:
    # Считаем сумму очков
    total = sum(points[ch] for ch in phrase)
    print("Общее количество очков:", total)