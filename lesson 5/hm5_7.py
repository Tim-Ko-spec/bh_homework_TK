#Есть список чисел: some_list = [111, 222, 333, 444, 555, 666, 777, 888, 999]. 
#* Нужно преобразовать его в одну строку, где элементы списка разделены '---' 
#** дополнительно каждый элемент списка разделить дефисом не использовать for и lambda

some_list = [111, 222, 333, 444, 555, 666, 777, 888, 999]

# Вспомогательная функция для разделения цифр дефисами
def split_digits(number):
    return '-'.join(str(number))

# Преобразуем список 
processed = list(map(split_digits, some_list))

# Объединяем элементы списка через '---'
result = '---'.join(processed)

print(result)