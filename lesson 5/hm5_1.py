#Дан произвольный список. Вывести на экран в обратном порядке. Задачу решить 2мя способами.

my_list = [1, 2, 3, 4, 5]

# Способ 1: Срез (синтаксис [::-1])
reversed_list_1 = my_list[::-1]
print("Способ 1 (срез):", reversed_list_1)

# Способ 2: Метод reverse() (изменяет список на месте)
my_list_copy = my_list.copy()  # чтобы не менять оригинальный список
my_list_copy.reverse()
print("Способ 2 (reverse()):", my_list_copy)