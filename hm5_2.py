#Запросить по очереди у пользователя 5 имен. 
#Добавить все в список. 
#Отсортировать. Вывести на экран. 
#Вывести True при наличии в списке имени 'Вася'

names = []

# Запрашиваем 5 имён у пользователя
for i in range(5):
    name = input(f"Введите имя {i + 1}: ")
    names.append(name)

# Сортируем список
names.sort()

# Выводим отсортированный список
print("Отсортированный список имён:", names)

# Проверяем наличие 'Вася' в списке
print("Есть ли 'Вася' в списке?", 'Вася' in names)