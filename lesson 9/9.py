# Написать функцию dict_from_args, которая принимает неограниченное
# количество позиционных аргументов и неограниченное количество аргументов
# ключевых-слов.

# Если все позиционные аргументы - целые числа, то рассчитать их сумму. Если
# нет, то кинуть ошибку TypeError("Все позиционные аргументы должны быть целыми").

# Если все именованные аргументы - ключевые слова являются строками, то найти максимальную
# длину слова. Если нет, то кинуть ошибку TypeError("Все аргументы - ключевые
# слова должны быть строками").

# Функция должна вернуть словарь, вида:
# {
#     "args_sum": 13,
#     "kwargs_max_len": 7
# }


def dict_from_args(*args, **kwargs):
    """
    Функция принимает неограниченное количество позиционных и именованных аргументов.
    - Если все позиционные аргументы целые числа -> считает их сумму.
    - Если среди них есть нецелые -> ошибка TypeError.
    - Если все ключи именованных аргументов строки -> ищет максимальную длину слова.
    - Если среди ключей есть нестроки -> ошибка TypeError.
    Возвращает словарь с результатами.
    """
    # Проверка позиционных аргументов
    if not all(isinstance(a, int) for a in args):
        raise TypeError("Все позиционные аргументы должны быть целыми")
    args_sum = sum(args)

    # Проверка ключей именованных аргументов
    if not all(isinstance(k, str) for k in kwargs.keys()):
        raise TypeError("Все аргументы - ключевые слова должны быть строками")
    kwargs_max_len = max(len(k) for k in kwargs.keys()) if kwargs else 0

    return {
        "args_sum": args_sum,
        "kwargs_max_len": kwargs_max_len
    }


print(dict_from_args(1, 2, 3, 7, day=18, temperature=22))  
# {"args_sum": 13, "kwargs_max_len": 11}

print(dict_from_args(5, 8, 0, hello="world", key="value"))  
# {"args_sum": 13, "kwargs_max_len": 5}